

class BirthDate:
    def __init__(self, bday, bmonth, byear):
        self.bday = bday
        self.bmonth = bmonth
        self.byear = byear


class Contact:
    def __init__(self, firstname, lastname, nickname, company, address, birthDate, notes):
        self.firstname = firstname
        self.lastname = lastname
        self.nickname = nickname
        self.company = company
        self.address = address
        self.birthDate = birthDate
        self.notes = notes

